my_dogs = 2 # integer
print(my_dogs)  # 2
print(type(my_dogs))  # <class 'int'>
my_dogs = ['Sammy', 'Frankie'] # list
print(my_dogs)  # ['Sammy', 'Frankie']
# The variable my_dogs was initially assigned the integer 2. However, it was reassigned to the list ['Sammy', 'Frankie'].
# This is perfectly valid in Python. The variable my_dogs was reassigned to a different data type, a list.
# This is unlike other programming languages where the data type of a variable is fixed.
# In Python, you can reassign a variable to a different data type.
# This is a powerful feature of Python and allows for greater flexibility in your code.
# However, it can also lead to confusion if you're not careful with variable assignments.
# Make sure to keep track of the data type of your variables when reassigning them to different values.
# This can help prevent bugs and unexpected behavior in your code.
