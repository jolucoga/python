# Integers and floats
# Integers are whole numbers, positive or negative. For example: 2 and -2 are examples of integers.
# Floats are real numbers with a decimal point dividing the integer and fractional parts. For example: 1.11, 2.0 and -2.1 are examples of floats.
# Strings
# Strings are sequences of characters, using the syntax of either single or double quotes: 'hello' is the same as "hello".
# Lists
# Lists are ordered sequences of values. A list begins with [ and ends with ]. Each value is separated by a comma. For example, [1, 2, 3, 4] is a list.
# Dictionaries
# Dictionaries are unordered collections of data in a key:value pair form. A dictionary begins and ends with curly braces {}. Each key is separated by a colon and each key:value pair is separated by a comma. For example, {'key1': 1, 'key2': 2, 'key3': 3} is a dictionary.
# Tuples
# Tuples are ordered immutable collections of data. A tuple begins with ( and ends with ). Each value is separated by a comma. For example, (1, 2, 3) is a tuple.
# Sets
# Sets are unordered collections of unique values. A set begins with { and ends with }. Each value is separated by a comma. For example, {1, 2, 3} is a set.
# Booleans
# Booleans are logical values, either True or False.
# None
# None is a special type that represents the absence of a value.
